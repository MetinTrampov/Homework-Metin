<?php

//Правим променливи start и end за начало и край на цикъла
$start = 1;
$end = 30;
//Правим променливата summ  за сбора  на числата 
$summ = 0;
//Правим цикъл който да върти от start до end по зададените стойности.
for ($cols = $start; $cols <= $end; $cols++) {
//Прибавяме към променливата sum всяко едно от числата в цикъла
    $summ += $cols;
}
//Изписваме на конзолата сумата от числата от началото до края
echo "Сумата от " . $start . " до " . $end . " = " . $summ;