<?php

//Правим for цикъл който ще добавя по 1 звезда на всеки един ред с командата echo "*" в цикъла.
for ($rows=0; $rows<5; $rows++)
{ 
    
    echo "*";
    //И правим друг вложен цикъл който добавя по още една звезда на всеки  ред
    for ($star=0; $star<$rows; $star++)
    {   
       echo "*";
       
    }
    echo "</br>";  
}
//Правим още един цикъл подобен на  горния , но в обратна посока да подрежда звездите в низходящ ред.
for ($rows=4; $rows>=0; $rows--)
{ 
    
    echo "*";
    
    for ($star=0; $star<$rows; $star++)
    {   
       echo "*";
       
    }
    echo "</br>";  

}