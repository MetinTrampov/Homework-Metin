{
    //Проверяваме ако променливата е стигнала до 10 и ако е стигнала трябва да спре цикъла без да пише последното тире
    if($rows===10)
    {
       echo $rows;
       break; 
    }
    //Изписва тире след всяко число
    echo $rows.'-';     
}





